public class Lager {
	
	public void auslagern(String name, String menge) {
		System.out.println("Artikel "+name+" in einer Menge von "+menge+" Stk. wurde ausgelagert");
	}
	
	public void einlagern(String name, String menge) {
		System.out.println("Artikel "+name+" in einer Menge von "+menge+" Stk. wurde eingelagert");
	}
}
